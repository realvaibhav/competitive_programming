#include<bits/stdc++.h>
using namespace std ;
typedef long long int ll ;
int solution()
{
   ll n ;
   cin >> n ;
   double d=1 ;
   d=sqrt(n);
   cout << round(d) << "\n" ;
   
   return 0;
}

int main()
{
    ll t ;
    cin >> t ;
    for (ll i = 0; i < t; i++)
    {
        solution();
    }

    return 0;
}